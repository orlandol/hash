  unsigned Bit2Hash( char* ident ) {
    uint16_t hashCode = 3;
    uint8_t ch;

    if( ident ) {
      ch = *ident++;
      while( ch ) {
        if( (ch == '_') || (isalnum(ch) != 0) ) {
          hashCode ^= bit2Table[ch];
          hashCode = (hashCode >> 5) | (hashCode << 11);
          hashCode += 10141;
        }

        ch = *ident++;
      }
    }

    return hashCode;
  }

